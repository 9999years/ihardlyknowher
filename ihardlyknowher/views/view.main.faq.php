	<h2>What is I Hardly Know Her?</h2>
	<p>IHKH is a free application to view public Flickr photostreams with minimal distractions.  It was created by <a href="http://justinouellette.com/">Justin Ouellette</a> and is not endorsed or certified by Flickr in any way.</p>
	
	<h2>How does it work?</h2>
	<p>IHKH uses <a href="http://www.flickr.com/services/">Flickr Services</a> to retrieve photo information, and all images are served directly from Flickr.  It will only display photos listed as &ldquo;public.&rdquo;  Changes to a photostream may lag up to 15 minutes due to caching.</p>

	<h2>How do sets work?</h2>
	<p>You can see a list of sets at <em>ihardlyknowher.com/yourname/sets</em> and access an individual set by adding a slash and either the set number or the name of the set (removing all non-alphanumeric characters, including punctuation and whitespace).</p>
	
	<h2>Can I customize how my photos are displayed?</h2>
	<p>Flickr users can dictate how their photos are displayed on IHKH.  Make sure you&rsquo;re logged into Flickr, then <a href="/user.settings">click here</a> to authenticate.  You will be redirected back to the IHKH settings page.  Site-wide display preferences are no longer available.</p>
	
